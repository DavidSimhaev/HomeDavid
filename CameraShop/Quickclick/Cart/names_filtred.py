class Names_Filtred:
    def __init__(self, name) -> None:
        self.name = name
        
    def replace_name_filtred(self):
        if self.name == "Filtred_Camers":
            return "Camera"
        if self.name == "Lens_filtred":
            return "lens"
        if self.name == "Lightings_filtred":
            return "lightings"
        if self.name == "Tripods_filtred":
            return "tripods"
        if self.name == "Tripods_filtred":
            return "tripods"
        if self.name == "binoculars_filtred":
            return "Binoculars"